package animation;

public class AnimationImage {
	/**
	 * Simple Object class to store the image's co-ordinates.
	 */
	private int x;
	private int y;

	/**
	 * Get method to get X corr
	 */
	public int getX() {
		return x;
	}

	/**
	 * Set method to set corr
	 */
	public void setX(int x2) {
		x = x2;
	}

	/**
	 * Get method to get Y corr
	 */
	public int getY() {
		return y;
	}

	/**
	 * Set method to set corr
	 */
	public void setY(int y2) {
		y = y2;
	}
}
